package string;

public class ConcatString {
    public static void main(String[] args) {
        String s = "Manoj";
        System.out.println(s);

        s.concat(" Kumar");
        System.out.println(s);

        s = s.concat(" Saini");
        System.out.println(s);

        s.concat(" Man");
        System.out.println(s);

        s = s.concat(" Man");
        System.out.println(s);
    }
}
